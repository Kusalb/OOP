#include <iostream>
using namespace std;

int main(){
	int a, b;
	a=7;
	b=2;
	cout <<" the value of a and b is " << a <<"and " << b <<endl; 
	//Printing the sum of a and b;
	cout << "a + b  = " << (a+b) <<endl;
	//Printing the difference of a and b;
	cout << "a - b  = " << (a-b) <<endl;
	//Printing the product of a and b;
	cout << "a * b  = " << (a*b) <<endl;
	//Printing the division of a and b;
	cout << "a / b  = " << ((float)a/b) <<endl;
	return 0;
}
